package com.example.tripschedule;

public class BusStation {
    private String Name;
    private int code;

    public BusStation(){}
    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }



}
